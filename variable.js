// 3 ways we can declare variable
// 1. var keyword - we will not use this keyword to declare a variable. We can use this for testing or checking
// 2. let keyword - We will use this when our data or variable is changeable
// 3. const keyword - When our data is fixed or unchangeable then we will use const keyword to declare a variable

var isLearner = true;
// when we need to see our output/result we will use console.log(something)
console.log(isLearner);
let age = 19;
console.log(age);
const fullName = "Ashraful Islam";
console.log(fullName);
